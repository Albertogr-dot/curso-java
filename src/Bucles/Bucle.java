package Bucles;

public class Bucle {
    static void main(String[] args) {
        for (int i=0; i<5; i++){
            System.out.println("El valor es: " + i );
        }
        System.out.println("Fin");

        int contador=0;
        while(contador<5){
            System.out.println("contador: " + contador);
            contador++;
        }
        System.out.println("Fin");

    }
}
